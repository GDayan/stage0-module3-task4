package school.mjc.stage0.module3.task4;

public class SettingTypes {
    public static void main(String[] args) {
        long first = 10_000_000_000L; // long for large integer value
        char second = 'a'; // char for single character
        boolean third = 5 > 9; // boolean for true/false value
        double forth = 12.90434323324342; // double for floating-point value
        float fifth = 12.3F; // float for single-precision floating-point value
        byte sixth = 1; // byte for small integer value (-128 to 127)
        short seventh = 129; // short for small integer value (-32,768 to 32,767)
    }
}

